# Other comparisons that returns False:
print(5 > 6)

print(4 in [1,2,3])

print("hello" == "goodbye")

print(5 == 6)

print(5 == 6 or 6 == 7)

print(5 == 6 and 6 == 7)

print(not(5 == 5))

print(3 not in [1,2,3])
