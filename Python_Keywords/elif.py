 # The elif keyword is used in conditional statements (if statements), and is short for else if.
 # Print "YES" if the variable i is a positive number, print "WHATEVER" if i is 0, otherwise print "NO":
for i in range (-5,5):
    if i > 0:
        print("YES")
    elif i == 0:
        print("WHATEVER")
    else:
        print("NO")
