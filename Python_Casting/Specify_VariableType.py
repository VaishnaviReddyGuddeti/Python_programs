# Casting in python is therefore done using constructor functions:

# 1.int() - constructs an integer number from an integer literal, a float literal , or a string literal
# 2.float() - constructs a float number from an integer literal, a float literal or a string literal
# 3. str() - constructs a string from a wide variety of data types, including strings, integer literals and float literals

# INTEGERS :
x = int(1)
y = int(2.8)
z = int("3")
print(x)
print(y)
print(z)

# FLOATS :
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)
print(y)
print(z)
print(w)

# STRINGS :
x = str("s1")
y = str(2)
z = str(3.0)
print(x)
print(y)
print(z)
