# islower() - Returns True if all characters in the string are lower case

# Check if all the characters in the text are in lower case:
txt = "hello world!"

x = txt.islower()

print(x)

# Check if all the characters in the texts are in lower case:
a = "Hello world!"
b = "hello 123"
c = "mynameisPeter"

print(a.islower())
print(b.islower())
print(c.islower())
